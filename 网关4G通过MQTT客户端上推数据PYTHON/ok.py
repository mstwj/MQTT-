def read_all_data():
    data_list = []
    for name, reg, scale, unit, pid in points:
        val = round(read_hold(METER_ADDR, reg) * scale, 1)
        data_list.append({"id": pid, "name": name, "value": val})
    return data_list

# ---------------------- 4G ----------------------
def connect_net():
    try:
        lte = network.LTE()
        lte.active(True)
        time.sleep(2)
        lte.connect()
        for _ in range(30):
            if lte.isconnected():
                print("✅ 4G 连接成功")
                return True
            time.sleep(1)
    except:
        return False

# ---------------------- MQTT ----------------------
def connect_mqtt():
    try:
        c = MQTTClient(MQTT_CLIENT_ID, MQTT_BROKER, MQTT_PORT, keepalive=60)
        c.connect()
        time.sleep(1)
        print("✅ MQTT 连接成功")
        return c
    except:
        print("❌ MQTT 连接失败")
        return None

# ---------------------- 主程序：一次发20条，间隔1秒发完 ----------------------
def main():
    fail_count = 0

    # 自动连4G
    print("正在连接4G...")
    if not connect_net():
        print("❌ 4G 连接失败，系统重启")
        machine.reset()

    # 自动连MQTT
    mqtt_client = connect_mqtt()
    while not mqtt_client:
        time.sleep(2)
        mqtt_client = connect_mqtt()

    print("开始上传数据（20条/包，1秒间隔）...\n")

    # 循环采集 + 分批发送
    while True:
        try:
            # 一次性采集所有 244 个数据
            all_data = read_all_data()
            batch_size = 20  # 每次发20个
            total = len(all_data)

            # 分批发送
            for i in range(0, total, batch_size):
                batch = all_data[i:i+batch_size]
                payload = json.dumps({"sn": SN, "device": batch})

                print("="*50)
                print(f"📤 发送第 {i//batch_size + 1} 批，共 {len(batch)} 条")
                print(payload)
                print("✅ 发送成功")
                print("="*50)
                print()

                mqtt_client.publish(MQTT_TOPIC, payload)
                time.sleep(1)  # 下一批等1秒

            fail_count = 0
            print("✅ 244个点位全部上传完成！\n")

        except:
            fail_count += 1
            print(f"❌ 发送失败，累计：{fail_count} 次")

            if fail_count >= 3:
                print("🔴 连续失败3次，硬件重启...")
                time.sleep(1)
                machine.reset()

            mqtt_client = connect_mqtt()

        time.sleep(10)  # 全部发完后等10秒再采集新一轮

# 自动运行
main()
