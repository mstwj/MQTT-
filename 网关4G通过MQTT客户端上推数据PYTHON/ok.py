def crc16(data):
    crc = 0xFFFF
    for byte in data:
        crc ^= byte
        for _ in range(8):
            if crc & 1:
                crc >>= 1
                crc ^= 0xA001
            else:
                crc >>= 1
    return crc.to_bytes(2, 'little')

def read_hold(addr, reg):
    try:
        uart.read()
        time.sleep(0.1)
        cmd = bytearray([addr, 0x03, (reg >> 8) & 0xFF, reg & 0xFF, 0x00, 0x01])
        cmd += crc16(cmd)
        uart.write(cmd)
        time.sleep(0.3)
        res = uart.read()
        if not res or len(res) < 7:
            return 0
        val = (res[3] << 8) | res[4]
        return val
    except:
        return 0

